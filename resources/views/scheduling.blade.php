<script>
  $(function () {

    /* initialize the external events
     -----------------------------------------------------------------*/


    /* initialize the calendar
     -----------------------------------------------------------------*/
    //Date for the calendar events (dummy data)
    var date = new Date()
    var d    = date.getDate(),
        m    = date.getMonth(),
        y    = date.getFullYear()

    var Calendar = FullCalendar.Calendar;
    var Draggable = FullCalendar.Draggable;

    var containerEl = document.getElementById('external-events');
    var checkbox = document.getElementById('drop-remove');
    var calendarEl = document.getElementById('calendar');

    // initialize the external events
    // -----------------------------------------------------------------

    new Draggable(containerEl, {
    itemSelector: '.external-event',
    eventData: function(eventEl) {
        // Code for converting the external event to eventObject
    },
    // Add the following line to prevent dragging of external events
    dragRevert: true
    });


    var calendar = new Calendar(calendarEl, {
      headerToolbar: {
        left  : 'prev,next today',
        center: 'title',
        right : 'dayGridMonth,timeGridWeek,timeGridDay'
      },
      themeSystem: 'bootstrap',
      //Random default events
    //   <!-- the events -->
                  events: [
                    @foreach($tasks as $task)
                    {
                        title: '{{ $task->name }}',
                        start: '{{ $task->task_date }}',
                        backgroundColor: '{{ $task->color }}',
                        borderColor: '{{ $task->color }}',
                        url: '{{ route('tasks.edit', $task->id) }}'
                    },
                    @endforeach
      ],
      editable  : true,
      droppable : true, // this allows things to be dropped onto the calendar !!!
      drop      : function(info) {
        // is the "remove after drop" checkbox checked?
        if (checkbox.checked) {
          // if so, remove the element from the "Draggable Events" list
          info.draggedEl.parentNode.removeChild(info.draggedEl);
        }
      }
    });

    calendar.render();
    // $('#calendar').fullCalendar()

    $('#add-new-task').click(function (e) {
  e.preventDefault();
  var taskTitle = $('#new-task-title').val();
  var taskDate = $('#new-task-date').val();

  if (taskTitle.length == 0 || taskDate.length == 0) {
    return;
  }

  // Make an AJAX request to create the task in Laravel
  $.ajax({
    url: '{{ route('tasks.store') }}',
    type: 'POST',
    data: {
      title: taskTitle,
      date: taskDate,
      // Additional data if needed
    },
    success: function(response) {
      // Handle the success response (e.g., display a success message)
      console.log(response);

        // Create an event object for the new task
        var newTaskEvent = {
        title: response.title,
        start: response.date,
        backgroundColor: response.color,
        borderColor: response.color,
        url: '{{ route('tasks.edit', '') }}' + '/' + response.id
        };

        // Add the new task event to the calendar
        calendar.addEvent(newTaskEvent);
    },
    error: function(error) {
      // Handle the error response (e.g., display an error message)
      console.log(error);
    }
  });

  // Clear the input fields
  $('#new-task-title').val('');
  $('#new-task-date').val('');
});
























    /* ADDING EVENTS */
    var currColor = '#3c8dbc' //Red by default
    // Color chooser button
    $('#color-chooser > li > a').click(function (e) {
      e.preventDefault()
      // Save color
      currColor = $(this).css('color')
      // Add color effect to button
      $('#add-new-event').css({
        'background-color': currColor,
        'border-color'    : currColor
      })
    })
    $('#add-new-event').click(function (e) {
      e.preventDefault()
      // Get value and make sure it is not null
      var val = $('#new-event').val()
      if (val.length == 0) {
        return
      }

      // Create events
      var event = $('<div />')
      event.css({
        'background-color': currColor,
        'border-color'    : currColor,
        'color'           : '#fff'
      }).addClass('external-event')
      event.text(val)
      $('#external-events').prepend(event)

      // Add draggable funtionality
      ini_events(event)

      // Remove event from text input
      $('#new-event').val('')
    })
  })
